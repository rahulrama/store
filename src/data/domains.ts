export const DOMAIN_BY_ID = Object.fromEntries(DOMAINS.map((d) => [d.id, d])) as Record<
  Domain['id'],
  Domain
>

export const PILLAR_BY_ID = Object.fromEntries(PILLARS.map((p) => [p.id, p])) as Record<
  Pillar['id'],
  Pillar
>

export function pillarOfDomain(domainId: Domain['id']): Pillar {
  return PILLAR_BY_ID[DOMAIN_BY_ID[domainId].pillarId]
}
